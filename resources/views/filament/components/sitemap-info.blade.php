<div style="line-height: 1.6;">
    <p><strong>Sitemap là gì?</strong></p>
    <p>Sitemap là file XML chứa danh sách tất cả các trang web của bạn, giúp các công cụ tìm kiếm như Google dễ dàng
        khám phá và lập chỉ mục nội dung.</p>
    <p><strong>Hướng dẫn tạo Sitemap:</strong></p>
    <ul>
        <li>Truy cập <a href="https://www.xml-sitemaps.com" target="_blank" style="color: #2563eb; text-decoration: underline;">https://www.xml-sitemaps.com</a> để tạo
            sitemap tự động</li>
        <li>Nhập URL trang web của bạn và tải về file sitemap.xml</li>
        <li>Upload file sitemap.xml vào đây để cập nhật</li>
    </ul>
</div>
